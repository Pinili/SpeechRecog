private void codebookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_codebookButtonActionPerformed
    JFileChooser fc = new JFileChooser();
    if(saveCheck.isSelected()){
        if(JFileChooser.APPROVE_OPTION == fc.showSaveDialog(this)){
            codebookFileName.setText(fc.getSelectedFile().getAbsolutePath());
        }
    }
    else{
        if(JFileChooser.APPROVE_OPTION == fc.showOpenDialog(this)){
            codebookFileName.setText(fc.getSelectedFile().getAbsolutePath());
        }
    }
}//GEN-LAST:event_codebookButtonActionPerformed

private void saveCheckItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_saveCheckItemStateChanged
    if(saveCheck.isSelected())
        codebookFileName.setEditable(true);
    else
        codebookFileName.setEditable(false);
}//GEN-LAST:event_saveCheckItemStateChanged

private void samplesButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_samplesButtonActionPerformed
    JFileChooser fc = new JFileChooser();
    fc.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
    if(JFileChooser.APPROVE_OPTION == fc.showOpenDialog(this)){
        samplesDirectoryName.setText(fc.getSelectedFile().getAbsolutePath());
    }
}//GEN-LAST:event_samplesButtonActionPerformed

