private void trainButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_trainButtonActionPerformed
    if( checkFields()){
        parent.setDirectoryPaths(samplesDirectoryName.getText(), testSamplesDirectory.getText());
        parent.setFileNames(indexFileName.getText(), listFileName.getText(), codebookFileName.getText());
        Main m = new Main(parent.getOrder(), parent.getCodebookSize(),
                          parent.getFrameLength(), parent.getFrameShift(),
                          parent.getStates(), null);
        m.train(codebookFileName.getText(), 
                    indexFileName.getText(), 
                    listFileName.getText(), 
                    samplesDirectoryName.getText(), 
                    testSamplesDirectory.getText());
        JOptionPane.showMessageDialog(this, "Finished training", "Status report", JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }
    else{
        JOptionPane.showMessageDialog(this, "Please fill in all details", "Invalid details", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_trainButtonActionPerformed

