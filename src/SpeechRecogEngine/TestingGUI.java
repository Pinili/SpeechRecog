private boolean checkFields(){
    if(!indexFileName.getText().equals("") 
            && !listFileName.getText().equals("")
            && !codebookFileName.getText().equals("") 
            && !order.getText().equals("")
            && !outputWordlist.getText().equals("")
            && !frameLength.getText().equals("") 
            && !frameShift.getText().equals("")
            && !codebookSize.getText().equals("")
            && !states.getText().equals("")){
        return true;
    }
    return false;
}

private void extractWordlist(String filename){
    int i;
    try {
        Scanner s = new Scanner(new File(filename)).useDelimiter("\\n");
        i = 0;
        while(s.hasNext()){
            s.next();
            i++;
        }
        output = new String[i];
        i = 0; s.close();
        s = new Scanner(new File(filename)).useDelimiter("\\n");
        while(s.hasNext()){
            output[i] = new String(s.next().trim());
            i++;
        }
        s.close();
    } catch (FileNotFoundException ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        dispose();
    }
}
  
