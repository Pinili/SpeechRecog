private void testButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_testButtonActionPerformed
    if( checkFields()){
        parent.setTestFileNames(listFileName.getText(),outputWordlist.getText(), testDirectory.getText());
        Main m = new Main(parent.getOrder(), parent.getCodebookSize(),
                          parent.getFrameLength(), parent.getFrameShift(),
                          parent.getStates(), null);
        extractWordlist(outputWordlist.getText());
        m.test(listFileName.getText(), testDirectory.getText(), 
               codebookFileName.getText(), indexFileName.getText(), output);
        // Show results
        JOptionPane.showMessageDialog(this, "Finished testing", "Status report", JOptionPane.INFORMATION_MESSAGE);
        dispose();
    }
    else{
        JOptionPane.showMessageDialog(this, "Please fill in all details", "Invalid details", JOptionPane.ERROR_MESSAGE);
    }
}//GEN-LAST:event_testButtonActionPerformed

